package com.javaindepth;

public class BitShifting {

    public static void main(String[] args) {
        /*
            Decimal     Binary
            8           00001000
            2           00000010
         */
        System.out.println(8 >> 2);

        /*
            Decimal     Binary
            8           00001000
            32          00100000
         */
        System.out.println(8 << 2);


    }
}
